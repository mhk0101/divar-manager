# -*- coding: utf-8 -*-
"""
HelpTab — راهنمای صفر تا صد برنامه و دکمه خروج کامل.
"""

from __future__ import annotations

from PySide6.QtCore import Qt
from PySide6.QtGui import QFont
from PySide6.QtWidgets import (
    QFrame,
    QGroupBox,
    QLabel,
    QScrollArea,
    QTextBrowser,
    QVBoxLayout,
    QWidget,
)


HELP_HTML = r"""
<div dir="rtl" style="font-family: Vazirmatn, Segoe UI, Tahoma; line-height: 1.9; font-size: 14px;">
  <h1>📘 راهنمای صفر تا صد Divar & Sheypoor Manager</h1>
  <p>
    این برنامه برای مدیریت حساب‌های <b>دیوار</b> و <b>شیپور</b>، ذخیره Session، اجرای اتوماسیون،
    استخراج آگهی، استخراج شماره تماس، ارسال پیام چت و زمان‌بندی اجرای خودکار ساخته شده است.
  </p>

  <h2>1) شروع کار</h2>
  <ol>
    <li>برنامه را اجرا کنید.</li>
    <li>از منوی سمت راست وارد تب <b>دیوار</b> یا <b>شیپور</b> شوید.</li>
    <li>اگر قبلاً وارد نشده‌اید، روی <b>ورود با شماره جدید</b> بزنید.</li>
    <li>شماره موبایل را وارد کنید.</li>
    <li>کد پیامک‌شده را وارد کنید.</li>
    <li>بعد از ورود موفق، Session حساب ذخیره می‌شود و دفعات بعد نیاز به ورود دوباره نیست.</li>
  </ol>

  <h2>2) تب دیوار و تب شیپور</h2>
  <p>در این دو تب می‌توانید حساب‌های ذخیره‌شده هر پلتفرم را مدیریت کنید.</p>
  <ul>
    <li><b>ورود با شماره جدید:</b> اضافه کردن حساب جدید.</li>
    <li><b>بررسی Session:</b> باز شدن مرورگر و بررسی معتبر بودن نشست.</li>
    <li><b>حذف Session:</b> پاک کردن حساب ذخیره‌شده از دیتابیس و فایل Session.</li>
    <li><b>بستن مرورگر:</b> بستن مرورگر اختصاصی همان تب.</li>
    <li><b>به‌روزرسانی لیست:</b> خواندن دوباره حساب‌های ذخیره‌شده.</li>
  </ul>

  <h2>3) Session چیست؟</h2>
  <p>
    Session همان وضعیت ورود شماست؛ شامل کوکی‌ها و داده‌های لازم برای اینکه برنامه بدون وارد کردن دوباره کد،
    حساب را باز کند. Sessionها در دیتابیس و فایل‌های JSON ذخیره می‌شوند.
  </p>
  <ul>
    <li>اگر Session معتبر باشد، برنامه مستقیم وارد حساب می‌شود.</li>
    <li>اگر Session منقضی شود، باید دوباره وارد حساب شوید.</li>
    <li>برنامه هنگام اجرای اتوماسیون تلاش می‌کند کوکی/توکن را زنده به‌روزرسانی کند.</li>
  </ul>

  <h2>4) تب اتوماسیون</h2>
  <p>تب اتوماسیون بخش اصلی اجرای عملیات است.</p>
  <h3>4.1 انتخاب پلتفرم</h3>
  <p>ابتدا مشخص کنید عملیات روی <b>دیوار</b> انجام شود یا <b>شیپور</b>.</p>

  <h3>4.2 انتخاب حساب</h3>
  <p>از لیست حساب‌ها، شماره‌ای را که می‌خواهید عملیات با آن انجام شود انتخاب کنید.</p>

  <h3>4.3 تعداد صفحات استخراج</h3>
  <p>
    مشخص می‌کند برنامه چند صفحه/اسکرول از لیست آگهی‌ها را بررسی کند. عدد بیشتر یعنی آگهی بیشتر،
    ولی زمان اجرای طولانی‌تر.
  </p>

  <h3>4.4 زمان بروزرسانی کوکی</h3>
  <p>
    اگر مقدار آن بیشتر از صفر باشد، برنامه در بازه مشخص‌شده کوکی‌ها و توکن‌های حساب را بررسی و ذخیره می‌کند.
    مقدار صفر یعنی غیرفعال.
  </p>

  <h2>5) انتخاب شهرها</h2>
  <ul>
    <li>در قسمت شهرها می‌توانید یک یا چند شهر انتخاب کنید.</li>
    <li>با جستجو می‌توانید شهر موردنظر را سریع پیدا کنید.</li>
    <li>دکمه <b>انتخاب همه</b> همه شهرهای نمایش‌داده‌شده را انتخاب می‌کند.</li>
    <li>دکمه <b>حذف انتخاب</b> همه انتخاب‌ها را پاک می‌کند.</li>
  </ul>
  <p><b>نکته:</b> اگر هیچ شهری انتخاب نشود، عملیات روی سراسر ایران/حالت پیش‌فرض پلتفرم انجام می‌شود.</p>

  <h2>6) انتخاب دسته‌بندی</h2>
  <ul>
    <li>می‌توانید یک دسته یا زیردسته را انتخاب کنید.</li>
    <li>اگر دسته‌ای انتخاب نشود یا «همه دسته‌ها» انتخاب باشد، آگهی‌ها بدون فیلتر دسته‌بندی استخراج می‌شوند.</li>
  </ul>

  <h2>7) استخراج شماره تماس و ارسال پیام چت</h2>
  <h3>7.1 استخراج هم‌زمان شماره و چت</h3>
  <p>
    اگر گزینه <b>استخراج هم‌زمان شماره تماس برای همان آگهی‌هایی که پیام چت فرستاده می‌شود</b> فعال باشد،
    برنامه برای هر آگهی این ترتیب را انجام می‌دهد:
  </p>
  <ol>
    <li>ورود به صفحه خود آگهی.</li>
    <li>لود شدن بخش دکمه‌های آگهی مثل «اطلاعات تماس» و «چت».</li>
    <li>کلیک روی «اطلاعات تماس» و استخراج شماره، اگر شماره موجود باشد.</li>
    <li>ذخیره شماره معتبر در فایل اکسل.</li>
    <li>اگر چت فعال باشد، ورود به صفحه چت همان آگهی.</li>
    <li>ارسال پیام چت.</li>
  </ol>
  <p>
    در دیوار برای استخراج شماره فقط بخش دکمه‌های آگهی لود می‌شود و برنامه منتظر لود کامل کل صفحه نمی‌ماند.
  </p>

  <h3>7.2 استخراج مجزای شماره</h3>
  <p>
    اگر حالت هم‌زمان غیرفعال باشد، می‌توانید استخراج شماره را جداگانه فعال کنید و سقف تعداد شماره‌ها را مشخص کنید.
  </p>

  <h3>7.3 ارسال پیام چت</h3>
  <p>
    اگر گزینه ارسال چت فعال باشد، باید متن پیام را وارد کنید. اگر متن خالی باشد، برنامه اجازه شروع عملیات نمی‌دهد.
  </p>

  <h2>8) ذخیره تنظیمات</h2>
  <p>
    با دکمه <b>ذخیره تنظیمات</b>، تنظیمات فعلی برای همان شماره و همان پلتفرم ذخیره می‌شود.
  </p>
  <ul>
    <li>تنظیمات دیوار و شیپور جدا ذخیره می‌شوند.</li>
    <li>شهرها، دسته‌بندی، تعداد صفحات، متن چت، سقف پیام، سقف شماره و زمان‌بندی ذخیره می‌شوند.</li>
    <li>وقتی همان شماره را انتخاب کنید، تنظیمات ذخیره‌شده همان پلتفرم بارگذاری می‌شود.</li>
  </ul>

  <h2>9) شروع عملیات</h2>
  <p>بعد از انتخاب پلتفرم، حساب، شهر، دسته‌بندی و تنظیمات استخراج، روی دکمه شروع بزنید.</p>
  <p>برنامه قبل از شروع و در حین عملیات، اینترنت را بررسی می‌کند. اگر اینترنت قطع شود، صبر می‌کند تا وصل شود و از همان مرحله ادامه می‌دهد.</p>

  <h2>10) جدول آگهی‌های استخراج‌شده</h2>
  <p>در پایین تب اتوماسیون، آگهی‌های استخراج‌شده نمایش داده می‌شوند:</p>
  <ul>
    <li>عنوان آگهی</li>
    <li>شماره تماس</li>
    <li>لینک آگهی</li>
    <li>وضعیت چت</li>
    <li>زمان استخراج</li>
  </ul>
  <p>می‌توانید لینک‌ها را کپی کنید یا جدول را پاک کنید.</p>

  <h2>11) خروجی فایل‌ها</h2>
  <ul>
    <li>آگهی‌ها در مسیر <code>data/extracted_ads</code> ذخیره می‌شوند.</li>
    <li>شماره‌های معتبر در مسیر <code>data/extracted_phones</code> به‌صورت Excel یا CSV ذخیره می‌شوند.</li>
    <li>فایل‌ها براساس پیش‌شماره، شهر/استان و دسته‌بندی پوشه‌بندی می‌شوند.</li>
  </ul>

  <h2>12) تب زمان‌بندی‌ها</h2>
  <p>
    زمان‌بندی‌ها برای اجرای خودکار عملیات استفاده می‌شوند. می‌توانید برای شماره‌ها و پلتفرم‌های مختلف چندین زمان‌بندی همزمان بسازید.
  </p>
  <ul>
    <li>هر زمان‌بندی در هر چرخه فقط یک‌بار اجرا می‌شود.</li>
    <li>وقتی همه زمان‌بندی‌های چرخه تمام شدند، چرخه بعدی از ابتدا ساخته می‌شود.</li>
    <li>در تب زمان‌بندی‌ها می‌توانید هر زمان‌بندی را توقف، ادامه، لغو یا حذف کنید.</li>
  </ul>

  <h2>13) تب لاگ‌ها</h2>
  <p>
    تمام پیام‌های سیستم، وضعیت عملیات، خطاها، نتیجه ورود، اعتبارسنجی Session، استخراج شماره و ارسال چت در تب لاگ‌ها نمایش داده می‌شود.
  </p>

  <h2>14) بستن مرورگر</h2>
  <p>
    اگر مرورگر باز مانده باشد، می‌توانید از دکمه <b>بستن مرورگر</b> در تب اتوماسیون یا سایدبار استفاده کنید.
  </p>

  <h2>15) نکات مهم و رفع مشکل</h2>
  <ul>
    <li><b>صفحه زوم شده است:</b> برنامه viewport و zoom را ثابت کرده تا صفحه برای همه اکانت‌ها کامل‌تر دیده شود.</li>
    <li><b>پیام چت ارسال نشد:</b> ممکن است چت برای آن آگهی فعال نباشد یا قبلاً پیام ارسال شده باشد.</li>
    <li><b>شماره استخراج نشد:</b> ممکن است شماره مخفی باشد، تماس امن/ناشناس باشد یا سایت نیاز به تأیید/کپچا داشته باشد.</li>
    <li><b>اینترنت قطع شد:</b> برنامه باید منتظر وصل شدن اینترنت بماند و ادامه دهد.</li>
    <li><b>Session نامعتبر شد:</b> از تب دیوار/شیپور دوباره وارد حساب شوید.</li>
  </ul>

  <h2>16) ترتیب پیشنهادی استفاده</h2>
  <ol>
    <li>ابتدا در تب دیوار یا شیپور وارد حساب شوید.</li>
    <li>در تب اتوماسیون پلتفرم و حساب را انتخاب کنید.</li>
    <li>شهر و دسته‌بندی را تنظیم کنید.</li>
    <li>تعداد صفحات، متن چت و سقف ارسال/استخراج را مشخص کنید.</li>
    <li>تنظیمات را ذخیره کنید.</li>
    <li>روی شروع بزنید یا زمان‌بندی بسازید.</li>
    <li>نتیجه را در جدول و لاگ‌ها بررسی کنید.</li>
  </ol>
</div>
"""


class HelpTab(QWidget):
    """تب راهنمای کامل برنامه + خروج کامل."""

    def __init__(self, parent=None):
        super().__init__(parent)
        self._setup_ui()

    def _setup_ui(self):
        outer = QVBoxLayout(self)
        outer.setContentsMargins(0, 0, 0, 0)

        scroll = QScrollArea()
        scroll.setWidgetResizable(True)
        scroll.setFrameShape(QFrame.NoFrame)

        container = QWidget()
        layout = QVBoxLayout(container)
        layout.setContentsMargins(24, 20, 24, 20)
        layout.setSpacing(16)

        title = QLabel("📘 راهنمای صفر تا صد برنامه")
        title.setObjectName("titleLabel")
        tf = QFont()
        tf.setPointSize(20)
        tf.setBold(True)
        title.setFont(tf)
        layout.addWidget(title)

        hint = QLabel("آموزش کامل همه بخش‌های برنامه، نحوه ورود، اتوماسیون، زمان‌بندی، خروجی‌ها و رفع مشکل.")
        hint.setObjectName("subtitleLabel")
        hint.setWordWrap(True)
        layout.addWidget(hint)

        group = QGroupBox("📚 راهنمای کامل")
        group_layout = QVBoxLayout(group)
        group_layout.setContentsMargins(14, 14, 14, 14)

        self.help_view = QTextBrowser()
        self.help_view.setOpenExternalLinks(True)
        self.help_view.setHtml(HELP_HTML)
        self.help_view.setMinimumHeight(520)
        group_layout.addWidget(self.help_view)
        layout.addWidget(group)

        scroll.setWidget(container)
        outer.addWidget(scroll)

    def restyle(self):
        try:
            self.help_view.style().unpolish(self.help_view)
            self.help_view.style().polish(self.help_view)
        except Exception:
            pass
